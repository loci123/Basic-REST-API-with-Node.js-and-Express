/**
 * @swagger
 * tags:
 *   name: Users
 *   description: User management
 */

/**
 * @swagger
 * /api/users/register:
 *   post:
 *     summary: Register a new user
 *     tags: [Users]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - name
 *               - email
 *               - password
 *             properties:
 *               name:
 *                 type: string
 *               email:
 *                 type: string
 *               password:
 *                 type: string
 *     responses:
 *       201:
 *         description: User created successfully
 *       400:
 *         description: Invalid input
 */

/**
 * @swagger
 * /api/users/login:
 *   post:
 *     summary: Authenticate a user
 *     tags: [Users]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - email
 *               - password
 *             properties:
 *               email:
 *                 type: string
 *               password:
 *                 type: string
 *     responses:
 *       200:
 *         description: Authentication successful
 *       400:
 *         description: Invalid credentials
 */

/**
 * @swagger
 * /api/users/me:
 *   get:
 *     summary: Get logged-in user's data
 *     tags: [Users]
 *     security:
 *       - bearerAuth: []
 *     responses:
 *       200:
 *         description: User data retrieved successfully
 *       401:
 *         description: Not authenticated
 */

/**
 * @swagger
 * /admin/getUsers:
 *   get:
 *     summary: Fetch all users (Admin only)
 *     tags: [Users]
 *     security:
 *       - bearerAuth: []
 *     responses:
 *       200:
 *         description: List of users retrieved successfully
 *       403:
 *         description: Not authorized as an admin
 */

const express = require('express');
const router = express.Router();
const { RegisterUser, loginUser, GetUser, GetAllUsers } = require('../controller/userController');
const { Protect, AdminProtect } = require('../middleware/authMiddleware');

router.post('/register', RegisterUser);
router.post('/login', loginUser);
router.get('/me', Protect, GetUser);
router.get('/admin/getUsers', Protect, AdminProtect, GetAllUsers);

module.exports = router;
